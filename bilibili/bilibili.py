row=['aid','view','danmaku','reply','favorite','coin','share']
'''
    aid：        视频编号
    view：       播放量
    danmaku：    弹幕数
    reply：      品论数
    favorite：   收藏数
    coin：       硬币数
    share：      分享数
'''
video=namedtuple('video',row)


def run(i):
    global lock
    global kill
    if kill==0:
        sys.exit()
    url="http://api.bilibili.com/archive_stat/stat?aid={}".format(i)
    response=get_response(url,i)
    if response and response['code']==0:
        try:
            data=response['data']
            Video=video(data['aid'],
                        data['view'],
                        data['danmaku'],
                        data['reply'],
                        data['favorite'],
                        data['coin'],
                        data['share'])
            with lock:
                result.append(Video)
                print('try:',i)

        except:
            pass

    if i % 10000 == 0:
        with lock:
            if result is not None:
                savenumber = int(i / 10000)
                save(savenumber)
                time.sleep(10)
            else:
                kill=0
    #固定的点保存已下载记录，若result为空则说明已没有数据，此时将kill赋值为0，将使所有的子进程退出


def get_response(url,i):
    try:
        response=requests.get(url,headers=headers,timeout=5).json()
    except Exception as e:
        print('请求异常Id：',i,'>>>',e)
        response=None
    time.sleep(0.5 + random.random())
    return response


def save(savenumber):

    with open('data_%s.csv'%str(savenumber),'w',newline='',encoding='utf-8') as f:
        #newline默认会在行之间插入空行
        f_csv=csv.writer(f)
        f_csv.writerow(row)
        f_csv.writerows(result)
    result.clear()


